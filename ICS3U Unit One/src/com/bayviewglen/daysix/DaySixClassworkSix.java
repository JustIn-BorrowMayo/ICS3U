/*
 * A 10 kilogram object is travelling at 12 metres per second.  Compute and print its momentum (mass times velocity)
 */

package com.bayviewglen.daysix;

public class DaySixClassworkSix {

	public static void main(String[] args) {
		
		int mass=10;
		int velocity=12;
		int momentum;
		
		momentum=mass*velocity;
		
		System.out.println("A 10 kilogram object travelling at 12 metres per second has a momentum of " + momentum + " kg m/s");
		

	}

}
